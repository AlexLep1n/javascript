"use strict";

function sum(num1, num2) {
    return console.log(num1 + num2);
}

function subtr(num1, num2) {
    return console.log(num1 - num2);
}

function mult(num1, num2) {
    return console.log(num1 * num2);
}

function segm(num1, num2) {
    return console.log(num1 / num2);
}

sum(3, 9);
subtr(4, 5);
mult(3, 7);
segm(10, 2);

